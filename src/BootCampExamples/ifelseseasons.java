package BootCampExamples;

public class ifelseseasons {
    public static void main(String[] args){
        int temp = 10;

        if(temp <=20){
            System.out.println("Winter");
        }
        else if(temp <=40){
            System.out.println("fall");
        }
        else if(temp<=60){
            System.out.println("spring");
        }
        else{
            System.out.println("summer");
        }
    }
}
